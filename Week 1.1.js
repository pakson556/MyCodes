//Program to perform addition, subtraction, multiplication and division.

const num1 = 30;
const num2 = 70;
const num3 = 2;
const num4 = 55;
const num5 = 7;
const num6 = 86

//Addition
let sum = num5 + num1;
console.log ("The sum of" , num5 , "and" , num1 , "is:" , sum);

//Subtraction
let sub = num2 - num4;
console.log ("The difference of" , num2 , "and" , num4 , "is:" , sub);

//Multiplication
let prod = num3 * num6;
console.log ("The product of", num3 , "and" , num6 , "is:" , prod);

//Division
let div = num1 / num5;
console.log ("The quotient of" , num1 , "and" , num5 , "is:", div);

//Complex
let total = num6 / num3 + (num2 * num5);
console.log ("The quotient of" , num6 , "and" , num3 , "plus the product of" , num2 , "and" , num5 , "is:" , total);